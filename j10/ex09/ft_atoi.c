#include "do_op.h"

int	ft_atoi(char *str)
{
	int	i;
	int	sign;
	int	res;

	sign = 0;
	i = 0;
	res = 0;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == ' '
			|| str[i] == '\r' || str[i] == '\v' || str[i] == '\f')
		i++;
	if (str[i] == '-')
		sign = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res *= 10;
		res += str[i] - '0';
		i++;
	}
	if (sign == -1)
		return (-res);
	else
		return (res);
}
